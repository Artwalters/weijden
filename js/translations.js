// Current language (default: Dutch)
let currentLanguage = localStorage.getItem('weijden-language') || 'nl';

// Function to get translation
function getTranslation(key, lang = currentLanguage) {
    const keys = key.split('.');
    let value = translations;
    
    for (const k of keys) {
        value = value[k];
        if (!value) {
            console.warn(`Translation key "${key}" not found`);
            return key;
        }
    }
    
    return value[lang] || value.nl || key;
}

// Function to translate elements with data-translate attribute
function translatePage(lang = currentLanguage) {
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = getTranslation(key, lang);
        
        if (element.tagName.toLowerCase() === 'input' && element.type === 'submit') {
            element.value = translation;
        } else if (element.hasAttribute('placeholder')) {
            element.placeholder = translation;
        } else if (element.hasAttribute('aria-label')) {
            element.setAttribute('aria-label', translation);
        } else {
            element.textContent = translation;
        }
    });
    
    // Update language
    currentLanguage = lang;
    localStorage.setItem('weijden-language', lang);
}

// Initialize translation system
function initializeTranslations() {
    // Set up language dropdown
    const languageDropdowns = document.querySelectorAll('.language-dropdown');
    const languageButtons = document.querySelectorAll('.language-btn');
    
    // Function to update current language display
    function updateCurrentLanguage(lang) {
        const flagMap = { nl: '🇳🇱', en: '🇬🇧', de: '🇩🇪' };
        const textMap = { nl: 'NL', en: 'EN', de: 'DE' };
        
        document.querySelectorAll('.language-current').forEach(current => {
            const flag = current.querySelector('.lang-flag');
            const text = current.querySelector('.lang-text');
            if (flag) flag.textContent = flagMap[lang];
            if (text) text.textContent = textMap[lang];
        });
    }
    
    // Set up dropdown toggle
    languageDropdowns.forEach(dropdown => {
        const current = dropdown.querySelector('.language-current');
        
        if (current) {
            current.addEventListener('click', (e) => {
                e.preventDefault();
                e.stopPropagation();
                
                // Close other dropdowns
                languageDropdowns.forEach(otherDropdown => {
                    if (otherDropdown !== dropdown) {
                        otherDropdown.classList.remove('active');
                    }
                });
                
                // Toggle current dropdown
                dropdown.classList.toggle('active');
            });
        }
    });
    
    // Set up language selection
    languageButtons.forEach(button => {
        button.addEventListener('click', (e) => {
            e.preventDefault();
            e.stopPropagation();
            
            const lang = button.getAttribute('data-lang');
            
            // Update active button
            languageButtons.forEach(btn => btn.classList.remove('active'));
            button.classList.add('active');
            
            // Update current language display
            updateCurrentLanguage(lang);
            
            // Close dropdowns
            languageDropdowns.forEach(dropdown => {
                dropdown.classList.remove('active');
            });
            
            // Translate page
            translatePage(lang);
        });
    });
    
    // Close dropdown when clicking outside
    document.addEventListener('click', (e) => {
        if (!e.target.closest('.language-dropdown')) {
            languageDropdowns.forEach(dropdown => {
                dropdown.classList.remove('active');
            });
        }
    });
    
    // Set initial language
    const savedLanguage = localStorage.getItem('weijden-language');
    if (savedLanguage && savedLanguage !== 'nl') {
        const activeButton = document.querySelector(`[data-lang="${savedLanguage}"]`);
        if (activeButton) {
            languageButtons.forEach(btn => btn.classList.remove('active'));
            activeButton.classList.add('active');
            updateCurrentLanguage(savedLanguage);
            translatePage(savedLanguage);
        }
    }
}

// Initialize when DOM is ready
document.addEventListener('DOMContentLoaded', initializeTranslations);